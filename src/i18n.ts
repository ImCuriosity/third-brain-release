export function getT(lang?: Lang): (key: TKey) => string {
	const l = lang ?? 'en';
	const dict = l === 'en' ? EN : KO;
	return (key: TKey) => dict[key] ?? KO[key];
}

export function jsonLangInstr(lang?: Lang): string {
	return lang === 'en'
		? 'All output in English. Return JSON only (no code blocks).'
		: '모두 한국어. JSON만 반환(코드블록 없이).';
}
